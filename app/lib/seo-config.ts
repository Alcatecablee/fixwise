// SEO Utility Functions
export const SEO_UTILS = {
  // Generate full keywords array
  getFullKeywords: (additionalKeywords: string[] = []): string[] => {
    return [
      ...SEO_CONFIG.PRIMARY_KEYWORDS,
      ...SEO_CONFIG.SECONDARY_KEYWORDS,
      ...SEO_CONFIG.SERVICE_KEYWORDS,
      ...additionalKeywords
    ];
  },
  
  // Generate page URL
  getPageUrl: (path: string): string => {
    return `${SEO_CONFIG.BASE_URL}${path}`;
  },
  
  // Generate structured data for a page
  getStructuredData: (type: 'software' | 'organization' | 'website', customData: any = {}) => {
    const baseData = SEO_CONFIG.STRUCTURED_DATA[type.toUpperCase() as keyof typeof SEO_CONFIG.STRUCTURED_DATA];
    return { ...baseData, ...customData };
  },
  
  // Validate SEO data
  validateSEOData: (data: any): boolean => {
    const required = ['title', 'description'];
    return required.every(field => data[field] && typeof data[field] === 'string' && data[field].length > 0);
  },
  
  // Generate meta description
  generateDescription: (content: string, maxLength: number = 160): string => {
    if (content.length <= maxLength) return content;
    return content.substring(0, maxLength - 3) + '...';
  },
  
  // Generate title
  generateTitle: (title: string, maxLength: number = 60): string => {
    if (title.length <= maxLength) return title;
    return title.substring(0, maxLength - 3) + '...';
  }
};

